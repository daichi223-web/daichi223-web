// Apply hints
let added = 0;
let skipped = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    if (hints[tk.id]) {
      if (tk.hint) {
        // already has hint, skip
        skipped++;
        continue;
      }
      tk.hint = hints[tk.id];
      added++;
    }
  }
}

