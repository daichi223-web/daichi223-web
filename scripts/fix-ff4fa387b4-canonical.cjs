// Token-id specific overrides (sentence/位置依存の特殊な hint)
const ID_SPECIFIC = {
  // s1 (もとは天皇命令〜倭比売命に剣を貰う、相模到着まで; 247 tokens)
  // 既存: s1-t? 倭建命 (固定 hint あり)
};

// Helper: build hint key
function getHintKey(tk) {
  const text = tk.text.replace(/[、。「」『』（）]/g, '').trim();
  const gt = tk.grammarTag || {};
  const pos = gt.pos || '';
  const meaning = gt.meaning || '';
  const baseForm = gt.baseForm || '';

  // Try specific keys first
  const candidates = [];
  if (meaning) candidates.push(`${text}|${pos}|${meaning}`);
  if (pos === '助動詞' && meaning) candidates.push(`${text}|${pos}|${meaning}`);
  if (pos === '現在推量') candidates.push(`${text}|現在推量`);
  candidates.push(`${text}|${pos}`);
  if (baseForm) candidates.push(`${baseForm}|${pos}`);
  return candidates;
}

// Apply hints
let added = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    if (tk.hint) continue; // 既存 hint は保持

    // ID-specific override?
    if (ID_SPECIFIC[tk.id]) {
      tk.hint = ID_SPECIFIC[tk.id];
      added++;
      continue;
    }

    const keys = getHintKey(tk);
    for (const k of keys) {
      if (PATTERNS[k] !== undefined) {
        if (PATTERNS[k] !== null) {
          tk.hint = PATTERNS[k];
          added++;
        }
        break;
      }
    }
  }
}

console.log(`Added ${added} new hints.`);

// ============================================================
// Step 4: 検証
// ============================================================
let okAlign = true;
for (const s of t.sentences) {
  const concat = s.tokens.map(tk => tk.text).join('');
  if (concat !== s.originalText) {
    console.log(`ALIGNMENT MISMATCH ${s.id}: tokens="${concat}" vs originalText="${s.originalText}"`);
    okAlign = false;
  }
}

const tokens = t.sentences.flatMap(s => s.tokens);
const meaningful = tokens.filter(tk => tk.layer > 0 || tk.grammarTag?.pos);
const wh = meaningful.filter(tk => tk.hint).length;
console.log(`sentences: ${t.sentences.length}`);
console.log(`tokens: ${tokens.length}`);
console.log(`meaningful tokens: ${meaningful.length}`);
console.log(`with hint: ${wh}`);
console.log(`coverage: ${Math.round((wh / meaningful.length) * 100)}%`);
console.log(`alignment ok: ${okAlign}`);

