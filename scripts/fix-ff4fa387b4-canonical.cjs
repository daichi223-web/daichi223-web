const fs = require('fs');
const path = require('path');

const fp = path.join(__dirname, '..', 'public', 'texts-v3', 'ff4fa387b4.json');
const t = JSON.parse(fs.readFileSync(fp, 'utf8'));

// ============================================================
// Step 1: s7 と s32 の重複削除 + sentence ID 再採番
// ============================================================

// s7 = "ここに、まづその御刀もちて...焼きたまひき。" (s6 末尾と重複)
// s32 = "ここに、倭にいます后たち　また御子たち...歌よみしたまひしく、" (s31 後半と重複)

const KEEP_IDS = new Set();
for (const s of t.sentences) {
  if (s.id !== 's7' && s.id !== 's32') KEEP_IDS.add(s.id);
}

t.sentences = t.sentences.filter(s => s.id !== 's7' && s.id !== 's32');

// sentence id を s1, s2, ... と再採番。token.id も s{n}-t{m} で再採番
let newId = 1;
const idMap = new Map(); // 旧 → 新
for (const s of t.sentences) {
  const oldId = s.id;
  const nid = 's' + newId;
  idMap.set(oldId, nid);
  s.id = nid;
  newId++;
}

// ============================================================
// Step 2: token.start/end/id を再計算
// ============================================================
let pos = 0;
for (const s of t.sentences) {
  let tIdx = 1;
  for (const tk of s.tokens) {
    tk.id = s.id + '-t' + tIdx;
    tk.start = pos;
    pos += tk.text.length;
    tk.end = pos;
    tIdx++;
  }
  // 各 sentence の originalText は元のまま (削除した sentence の content は他の sentence に含まれている)
}

