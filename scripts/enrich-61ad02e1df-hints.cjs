const fs = require('fs');
const path = require('path');

const fp = path.join(__dirname, '..', 'public', 'texts-v3', '61ad02e1df.json');
const distFp = path.join(__dirname, '..', 'dist', 'texts-v3', '61ad02e1df.json');
