function buildHint(tk) {
  if (tk.hint) return null;
  const g = tk.grammarTag || {};
  const text = tk.text;
  const pos = g.pos || '';
  const base = g.baseForm || '';
  const meaning = g.meaning || '';
  const form = g.conjugationForm || '';

  // ===== 大鏡 序 特有の語彙 =====
  if (text === '世継' || text === '世継ぎ' || text.includes('世継')) {
    return '名詞「世継 (大宅世継)」: 大鏡の語り手の一人。190 歳の翁という設定で、文徳天皇から後一条天皇までの 14 代の歴史を語る。「世継物語」の異名は彼に由来';
  }
  if (text === '繁樹') {
    return '名詞「繁樹 (夏山繁樹)」: 世継のもう一人の語り相手で、180 歳の翁。雲林院菩提講で世継と再会し、藤原氏の歴史を補足する役回り';
  }
  if (text === '菩提講') {
    return '名詞「菩提講 (ぼだいこう)」: 法華経八講の一種で、菩提を願う仏事。雲林院では毎年五月に行われ、貴賤老若が群集した。大鏡の枠物語の舞台';
  }
  if (text === '雲林院') {
    return '名詞「雲林院 (うりんゐん)」: 京都洛北紫野にあった天台宗の名刹。淳和天皇の離宮を仏寺としたもの。菩提講で名高く、大鏡の冒頭の舞台';
  }
  if (text === '翁') {
    return '名詞「翁 (おきな)」: 老人の男。「翁二人、嫗一人」が偶然出会い昔語りを始めるのが大鏡の枠組み';
  }
  if (text === '嫗') {
    return '名詞「嫗 (おうな)」: 老女・年取った女性。世継・繁樹と同席する 3 人目の語り手の一人';
  }
  if (text === '老人' || text === '老いたる') {
    return '名詞・形容詞「老いたる人」: 老人。語り手たちが自らの長寿を強調し、「直接見聞きした昔のこと」として語る権威付けの役割';
  }
  if (text === 'ありさま') {
    return '名詞「ありさま」: ようす・状態・経緯。「世のありさま」「事のありさま」など歴史の様相を語る大鏡の核語';
  }
  if (text === 'よし') {
    return '名詞「よし (由)」: ①事情・由来 ②理由 ③(連用形「よく」と異なる)。「物語せむと思ふぞよ」と話を切り出す前置き表現で頻出';
  }
  if (text === '物語' || text === 'むかし語り' || text === '昔物語') {
    return '名詞「物語・昔物語」: 過去の出来事の語り。大鏡では世継・繁樹が体験した歴史を「物語」として枠物語形式で語る';
  }
  if (text === '冠者') {
    return '名詞「冠者 (くわじゃ)」: 元服した若者・若い従者。世継たちの語りに割り込み質問する若侍。聴き手として批評的な立場で語りに緊張感を加える';
  }
  if (text === '侍' || text === '侍ふ') {
    return '名詞・動詞「侍 (さむらひ・はべり)」: 「さむらひ」は警護・側近の武士。動詞「はべり」は丁寧の補助動詞「〜ております」の用法も頻出';
  }
  if (text === '年ごろ') {
    return '重要古今異義語「年ごろ」: 長年・数年来 (副詞)。現代語「年頃 (適齢期)」と意味が違う。大鏡冒頭で「長年抱いてきた思い」を表明';
  }
  if (text === '心地') {
    return '名詞「心地 (ここち)」: 気持ち・気分・心持ち。「心地よし」「心地す」など複合で頻出';
  }
  if (text === 'ためし') {
    return '名詞「ためし (例)」: 先例・事例・前例。「過ぎにしためし」=過去の事例。歴史を語る正統性を示す語';
  }
  if (text === '帝' || text === '御門') {
    return '名詞「帝 (みかど)」: 天皇。大鏡では文徳天皇から後一条天皇までを「14 代の帝」として年代順に列伝形式で語る';
  }
  if (text === '世' || text === '御世') {
    return '名詞「世・御世 (みよ)」: 帝の御代・治世。「〜の御世」で具体的な治世の指定。大鏡の構成基軸';
  }
  if (text === '昔') {
    return '名詞「昔 (むかし)」: 古い時代。「直接見聞きした昔」として翁たちの語りを正当化';
  }
  if (text === 'うへ') {
    return '名詞「上 (うへ)」: ①身の上 ②目上の方 ③表面 ④主上 (天皇)。文脈で多義。';
  }
  if (text === 'おのれ' || text === 'おのれら' || text === 'おのが') {
    return '代名詞「おのれ」: ①一人称「私」(謙譲) ②二人称「おまえ」(目下に対して) ③再帰「自分」。世継・繁樹が自称として用いるのは①の謙譲用法';
  }
  if (text === '主' || text === '主や') {
    return '代名詞「主 (ぬし)」: 二人称「あなた・おまえさま」。やや敬意を込めた呼びかけ。世継が繁樹に対し、また聴衆に対し用いる';
  }
  if (text === 'これ') {
    return '代名詞「これ」: ①これ (近称) ②私 (一人称・謙譲)。「これら」=「これらの者ども」(世継・繁樹自身)';
  }
  if (text === 'これら') {
    return '代名詞「これら」: 複数の近称「この人たち・私たち」。世継・繁樹自身を指して謙遜しつつ語り手として位置付け';
  }
  if (text === 'こ') {
    return '代名詞「こ」: 近称「この」。「この〜」と物・事・人を近く指す代名詞語幹。';
  }
  if (text === 'さ') {
    return '指示副詞「さ」: そのように・そう。「さ申せば」「さおぼしめさん」など語り中の指示語として頻出';
  }
  if (text === 'いかが') {
    return '副詞「いかが」: ①どうして・どうやって (疑問) ②どうして〜か、いや〜ない (反語)。「いかが思す」=どう思われるか';
  }
  if (text === 'いかで' || text === 'いかでか') {
    return '副詞「いかで」: ①どうして (疑問・反語) ②どうにかして〜したい (願望、下に意志・願望)。「いかで〜む／いかで〜ばや」呼応で願望';
  }
  if (text === 'なほ') {
    return '副詞「なほ」: やはり・依然として・それでもなお。前提を踏まえて結論を強調する場合に頻出';
  }
  if (text === 'いと') {
    return '副詞「いと」: 非常に・たいそう。古文最頻出の程度副詞。打消を伴うと「あまり〜ない」(部分否定)';
  }
  if (text === 'いみじ' || text === 'いみじう' || text === 'いみじき' || text === 'いみじく') {
    return '重要語「いみじ」: 程度が甚だしい (良いにも悪いにも)・たいそう・ひどい。文脈で「素晴らしい」「ひどい」を見分ける';
  }
  if (text === 'あはれ' || text === 'あはれに' || text === 'あはれなる') {
    return '重要語「あはれなり」: しみじみと感動深い・しんみり心打たれる。古文の美意識の中心語。涙を誘う情感を表す';
  }
  if (text === 'うれしく' || text === 'うれし' || text === 'うれしき') {
    return '重要語「うれし」: 嬉しい・喜ばしい。シク活用形容詞。世継・繁樹の再会の感慨を端的に表現';
  }
  if (text === '心やすく' || text === '心やすし') {
    return '重要語「心やすし」: 気がかりがなく安心だ・気が置けない・親しい。連用形「心やすく」で「気軽に・親しく」';
  }
  if (text === 'おどろおどろし' || text === 'おどろおどろしき') {
    return '重要語「おどろおどろし」: 大げさだ・気味が悪い・不気味だ。物の怪・霊的存在の表現で頻出';
  }
  if (text === 'ふくるる' || text === 'ふくる') {
    return '動詞「ふくる」(ラ下二・夜が更ける): 夜が深くなる。「夜ふくれば」=夜が更けると、と語りが夜半まで及ぶことを示す';
  }
  if (text === 'おぼし' || text === 'おぼしめし' || text === 'おぼしめす') {
    return '尊敬動詞「思しめす・おぼし召す」: 「思ふ」の最高敬語。「思す」の更に上で、天皇・中宮級に用いる';
  }
  if (text === '聞こし召し' || text === '聞こし召す' || text === '聞こしめす') {
    return '尊敬動詞「聞こしめす」: 「聞く・食ふ・治む」の最高敬語。話を聞かれる、お召し上がりになる、治められる、で多義';
  }
  if (text === '言ひ' || text === 'いふ' || text === '言ひ集めなむ' || text === '言は' || text === '言へ' || text === '言ふ') {
    return '動詞「言ふ」(ハ四): 言う。語り全体の核となる動詞で、対象・引用・伝聞などとの組み合わせで多用される';
  }
  if (text === '見') {
    return '動詞「見る」(マ上一): 見る・体験する・男女が結婚生活を共にする。古文の上一段は「ひいきにみゐる」で覚える';
  }
  if (text === '聞く') {
    return '動詞「聞く」(カ四): ①聞く ②伝聞で知る ③(夫婦として) 通う・関係する。文脈で識別';
  }
  if (text === '思ふ' || text === '思ひ' || text === '思へ') {
    return '動詞「思ふ」(ハ四): 思う。「思ふに」「思ひつづく」など複合で頻出';
  }
  if (text === 'なる' || text === 'なら' || text === 'なり' || text === 'なれ') {
    if (meaning === '断定' || pos === '助動詞') {
      return '助動詞「なり」: 断定 (〜である)。体言・連体形に接続。連用形「に」+「あり」が原形で「にあり」の融合語';
    }
    return '動詞「なる」(ラ四): なる・変化する。古文では状態の変化や成立を表す重要動詞';
  }
  if (text === '対面し' || text === '対面す') {
    return '動詞「対面す」(サ変): 顔を合わせる・お目にかかる。「対面したまふ」=お会いになる、で敬意を含む';
  }
  if (text === '行きあひ' || text === '行きあふ') {
    return '動詞「行き会ふ」(ハ四): 道で偶然出会う・行き合う。世継と繁樹が雲林院の菩提講で再会する瞬間を描く';
  }
  if (text === '会ひ' || text === '会ふ') {
    return '動詞「会ふ」(ハ四): 出会う・対面する。「会ひまうす」で「お会い申し上げる」と謙譲を含む';
  }
  if (text === '見かはし' || text === '見かはす') {
    return '動詞「見交はす」(サ四): たがいに見合わせる・目を合わせる。再会の感慨を視覚的に描写する身振り';
  }
  if (text === '笑ひ' || text === '笑ふ') {
    return '動詞「笑ふ」(ハ四): 笑う。長寿の翁同士が再会して笑い合う、ほのぼのとした場面';
  }
  if (text === '同じ' || text === '同じく') {
    return '形容詞「同じ」(シク): 同じだ。連体形「同じき」または「同じ」(シク特殊型)。「同じ〜」で並列・比較';
  }
  if (text === 'されば') {
    return '接続詞「されば」: 「然 (さ) り＋ば」=「そうであるから・だから」。順接で前文を受けて結論を導く';
  }
  if (text === 'さて') {
    return '接続詞「さて」: ①そうして・それで ②さて (話題転換)。語りの流れを変える際に多用';
  }
  if (text === 'また') {
    return '副詞「また」: ①再び ②また・他に。並列・添加を示す';
  }

  // ===== 助動詞 =====
  if (pos === '助動詞') {
    if (base === 'なり' && meaning === '断定') {
      return '助動詞「なり」: 断定 (〜である)。体言・連体形に接続。「なりけり」で発見・気づきの含み';
    }
    if (base === 'けり') {
      return '助動詞「けり」: 過去 (〜た) ＋ 詠嘆 (〜だなあ)。連用形接続。和歌では詠嘆、地の文では伝聞過去が主';
    }
  }

  // ===== 動詞: 一般 =====
  if (pos === '動詞') {
    if (base === 'なる') return '動詞「なる」(ラ四): なる・成立する。状態変化・成立を表す基本動詞';
    if (base === 'いふ' || base === '言ふ') return '動詞「言ふ」(ハ四): 言う。語りの核となる動詞';
    if (base === '見る') return '動詞「見る」(マ上一): 見る・体験する';
    if (base === '思ふ') return '動詞「思ふ」(ハ四): 思う・考える';
    if (base === 'す') return 'サ変動詞「す」: する。「〜す」で複合し各種動作を表す万能動詞';
    return '動詞「' + (base || text) + '」: ' + (form ? form + '形。' : '') + (meaning ? meaning : '文脈の動作を表す') + '。';
  }

  // ===== 形容詞 =====
  if (pos === '形容詞') {
    if (base === 'よし') return '形容詞「よし」: 良い・身分が高い・上品だ。「よき〜」で身分・上質を示す';
    if (base === 'なし') return '形容詞「なし」(ク): 無い・存在しない';
    return '形容詞「' + (base || text) + '」: ' + (form ? form + '形。' : '') + (meaning || '状態を表す') + '。';
  }

  // ===== 補助動詞 =====
  if (pos === '補助動詞') {
    if (text === 'たまひ' || text === 'たまふ' || text === 'たまへ' || text === 'たまへる') {
      return '尊敬補助動詞「たまふ」(ハ四): 「〜なさる・お〜になる」。動詞連用形に付き、動作主への尊敬を示す';
    }
    if (text === 'はべり' || text === 'はべる' || text === 'はべら' || text === 'はべれ') {
      return '丁寧補助動詞「はべり」(ラ変): 「〜ております」。語り手から聴き手への敬意。大鏡の語り全体を貫く文体特徴';
    }
    if (text === 'まうす' || text === '申す' || text === 'まうし' || text === '申し') {
      return '謙譲補助動詞「申す・まうす」(サ四): 「〜し申し上げる」。動作の対象への敬意';
    }
    return '補助動詞「' + text + '」: 連用形に付き、敬意・継続・完了などを表す';
  }

  // ===== 助詞群 =====
  if (pos === '格助詞') {
    if (text === 'の' && meaning === '体修') {
      return '格助詞「の」(連体修飾): 「AのB」=「AのB」(所有・属性)。古文では主格用法 (〜が) も頻出';
    }
    if (text === 'と' && meaning === '引用') {
      return '格助詞「と」(引用): 「〜と思ふ・〜と言ふ」など発話・思考内容を承ける';
    }
    if (text === 'と' && meaning === '相手') {
      return '格助詞「と」(相手): 「〜と会ふ」「〜と語る」など共同動作の相手を示す';
    }
    if (text === 'を' && meaning === '対象') {
      return '格助詞「を」(対象): 動作の目的を示す。古文では起点 (〜から)・経過点 (〜を通って) の用法もあるので識別注意';
    }
    if (text === 'に' && meaning === '場所') {
      return '格助詞「に」(場所): 「〜に」で場所・存在の場を示す。古文の「に」は時・場所・対象・原因など多義';
    }
    if (text === 'に' && meaning === '対象') {
      return '格助詞「に」(対象): 動作の対象・帰着点。「〜に詣づ」=「〜に参拝する」';
    }
    if (text === 'に' && meaning === '結果') {
      return '格助詞「に」(結果): 「〜になる」など変化の結果を示す';
    }
    if (text === 'より' && meaning === '比較') {
      return '格助詞「より」(比較): 「〜より」=「〜より」(比較の基準)。経由・起点・手段の用法もある';
    }
    if (text === 'より') {
      return '格助詞「より」: 起点・経由・比較・手段を示す多義の格助詞';
    }
    if (text === 'にて') {
      return '格助詞「にて」(場所・手段): 「〜で・〜にて」。場所・手段・原因・状態を示す。「雲林院にて」=雲林院で';
    }
    return '格助詞「' + text + '」' + (meaning ? '(' + meaning + ')' : '') + ': 名詞と動詞の関係を示す';
  }

  if (pos === '係助詞') {
    if (text === 'は' && meaning === '提示') {
      return '係助詞「は」: 主題提示 (〜は)。古文では対比・強意の用法もあり、文脈で識別';
    }
    if (text === 'も' && meaning === '強意') {
      return '係助詞「も」: 強意 (〜も)。打消・反語と組むと反語強調';
    }
    if (text === 'も' && meaning === '並列') {
      return '係助詞「も」: 並列 (〜も〜も)。複数事項を並べる';
    }
    if (text === 'ぞ') {
      return '係助詞「ぞ」: 強意。文末を連体形で結ぶ係結。「〜ぞ〜連体形」';
    }
    if (text === 'なむ') {
      return '係助詞「なむ」: 強意。文末を連体形で結ぶ係結。「〜なむ〜連体形」';
    }
    if (text === 'こそ') {
      return '係助詞「こそ」: 強意。文末を已然形で結ぶ係結。「〜こそ〜已然形」';
    }
    if (text === 'や') {
      return '係助詞「や」: 疑問・反語。文末を連体形で結ぶ係結。文意で疑問・反語を識別';
    }
    if (text === 'か') {
      return '係助詞「か」: 疑問・反語。文末を連体形で結ぶ係結';
    }
    return '係助詞「' + text + '」: 強意・疑問・係結を作る';
  }

  if (pos === '接続助詞') {
    if (text === 'ば') {
      return '接続助詞「ば」: 未然形＋ば=順接仮定 (もし〜ならば)、已然形＋ば=順接確定 (〜ので・〜と)。識別必須';
    }
    if (text === 'て') {
      return '接続助詞「て」: 単純接続 (〜て)。連用形接続。前後の動作を順に並べる';
    }
    if (text === 'ど' || text === 'ども') {
      return '接続助詞「ど・ども」: 逆接確定 (〜けれども・〜のに)。已然形接続';
    }
    if (text === 'に' || text === 'を') {
      return '接続助詞「' + text + '」: 連体形に接続して理由・対比などの逆接・順接を示す';
    }
    return '接続助詞「' + text + '」: 文と文をつないで関係を示す';
  }

  if (pos === '副助詞') {
    if (text === 'ばかり') {
      return '副助詞「ばかり」: ①程度・限定 (〜くらい・〜ほど) ②時刻・所要時間。文脈で識別';
    }
    if (text === 'のみ') {
      return '副助詞「のみ」: 限定 (〜だけ・〜ばかり)';
    }
    if (text === 'など') {
      return '副助詞「など」: 例示 (〜など・〜たりなど)';
    }
    if (text === 'さへ') {
      return '副助詞「さへ」: 添加 (〜までも・〜さえ)。「だに」(類推) と区別';
    }
    if (text === 'だに') {
      return '副助詞「だに」: ①最小限 (せめて〜だけでも) ②類推 (〜さえ)';
    }
    return '副助詞「' + text + '」: 副次的な意味を添える';
  }

  if (pos === '終助詞') {
    if (text === 'かな') {
      return '終助詞「かな」: 詠嘆 (〜だなあ・〜ことよ)。連体形・体言に接続';
    }
    if (text === 'ばや') {
      return '終助詞「ばや」: 自己の願望 (〜したい)。未然形接続';
    }
    if (text === 'なむ') {
      return '終助詞「なむ」: 他者への願望 (〜してほしい)。未然形接続。係助詞・完了+推量の「なむ」と識別';
    }
    return '終助詞「' + text + '」: 文末で詠嘆・願望・命令などを示す';
  }

  if (pos === '代名詞') {
    return '代名詞「' + text + '」: 人・物・場所を指し示す';
  }

  if (pos === '副詞') {
    return '副詞「' + (base || text) + '」: ' + (meaning || '動作・状態を修飾') + '。';
  }

  if (pos === '接続詞') {
    return '接続詞「' + text + '」: 文と文をつないで論理関係を示す';
  }

  return null;
}

let added = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    const h = buildHint(tk);
    if (h) {
      tk.hint = h;
      added++;
    }
  }
}

