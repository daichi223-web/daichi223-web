function buildByPattern(tk) {
  if (tk.hint) return null;
  const g = tk.grammarTag || {};
  const text = tk.text;
  const pos = g.pos || '';
  const meaning = g.meaning || '';

  // ===== 助動詞 =====
  if (pos === '助動詞') {
    if (text === 'ず') return '助動詞「ず」: 打消 (〜ない)。未然形接続。「騒がず」=「取り乱さない」と良秀の異常な落ち着き。';
    if (text === 'けれ') return '助動詞「けり」已然形「けれ」: 過去 ＋ 詠嘆。「言ひければ」=「(人が)言ったので」と過去確定の順接。';
    if (text === 'る') return '助動詞「る」: 受身・尊敬・自発・可能 (4 用法)。連体形「る」で「立ちたまへる」=「立っていらっしゃる」(尊敬の補助動詞「たまふ」+ 存続「り」連体形「る」) など。';
    if (text === 'に' && meaning === '断定') return '助動詞「なり」連用形「に」: 断定 (〜で)。「にや」「にこそ」「にて」の核で、後ろに「あり」を補って読む。';
    if (text === 'けり') return '助動詞「けり」: 過去 ＋ 詠嘆。連用形接続。';
    return null;
  }

  // ===== 格助詞 =====
  if (pos === '格助詞') {
    if (text === 'の' && meaning === '体修') return '格助詞「の」(連体修飾): 「AのB」(所有・属性)。古文では主格 (〜が) の用法もあり、文脈で判別。';
    if (text === 'の' && meaning === '主格') return '格助詞「の」(主格): 「〜が」。連体修飾節中の主語で頻出。';
    if (text === 'を' && meaning === '対象') return '格助詞「を」(対象): 動作の目的を示す (〜を)。';
    if (text === 'に' && meaning === '場所') return '格助詞「に」(場所): 「〜に」で存在の場や帰着点。';
    if (text === 'に' && meaning === '対象') return '格助詞「に」(対象): 動作の対象や帰着点。';
    if (text === 'に' && meaning === '時間') return '格助詞「に」(時間): 「〜に」で時を示す。';
    if (text === 'に') return '格助詞「に」: 場所・時間・対象・原因など多義。文脈で判別。';
    if (text === 'と') return '格助詞「と」: 引用 (〜と思ふ・〜と言ふ) や並列 (〜と〜)。';
    if (text === 'にて') return '格助詞「にて」: 場所・手段・状態。「〜で」。';
    return '格助詞「' + text + '」' + (meaning ? '(' + meaning + ')' : '') + ': 名詞と他語の関係を示す。';
  }

  // ===== 接続助詞 =====
  if (pos === '接続助詞') {
    if (text === 'て') return '接続助詞「て」: 単純接続 (〜て)。連用形接続。';
    if (text === 'ば') return '接続助詞「ば」: 未然形＋ば=順接仮定、已然形＋ば=順接確定。識別必須。';
    return '接続助詞「' + text + '」: 文と文をつなぐ。';
  }

  // ===== 代名詞 =====
  if (pos === '代名詞' || pos === '代名') {
    return '代名詞「' + text + '」: 人・物・場所を指し示す。';
  }

  // ===== 名詞 =====
  if (pos === '名詞') {
    return '名詞「' + text + '」: 文中の対象・場所・概念を示す。';
  }

  return null;
}

let added = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    if (tk.hint) continue;
    let h = pinpoint[tk.id];
    if (!h) h = buildByPattern(tk);
    if (h) { tk.hint = h; added++; }
  }
}

let ok = true;
for (const s of t.sentences) {
  if (s.tokens.map(tk => tk.text).join('') !== s.originalText) ok = false;
}
