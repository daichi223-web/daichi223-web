// ── 適用関数 ──────────────────────────────────────
function pickHint(tk) {
  if (tk.hint) return null; // 既存維持
  const text = tk.text;
  const gt = tk.grammarTag || {};
  const pos = gt.pos;
  const baseForm = gt.baseForm;
  const meaning = gt.meaning;
  const cType = gt.conjugationType;
  const cForm = gt.conjugationForm;

  // 助動詞識別
  if (pos === '助動詞') {
    if (text === 'に' && meaning === '断定') return hintMap['ni-断定'];
    if (text === 'れ' && meaning === '受身') return hintMap['れ-受身'];
    if (text === 'れ' && meaning === '自発') return hintMap['れ-自発'];
    if (text === 'られ' && meaning === '受身') return hintMap['られ-受身'];
    if (text === 'るる' && meaning === '自発') return hintMap['るる-自発'];
    if (text === 'せ' && meaning === '尊敬') return hintMap['せ-使尊'];
    if (text === 'しめ' && meaning === '尊敬') return hintMap['しめ-使尊'];
    if (text === 'な' && meaning === '強意') return hintMap['na-強意'];
    if (text === 'ぬ' && meaning === '完了') return hintMap['ぬ-完了'];
    if (text === 'ぬ' && meaning === '打消') return hintMap['ぬ'];
    if (hintMap[text]) return hintMap[text];
    if (baseForm && hintMap[baseForm]) return hintMap[baseForm];
  }

  // 補助動詞 (敬語)
  if (pos === '補助動詞') {
    if (baseForm && hintMap['hon:' + baseForm]) return hintMap['hon:' + baseForm];
    // 補助動詞「あり」など
    if (baseForm === 'あり' || text === 'あら') return '動詞「あり」(ラ変): 「ある・いる」。「にあり/にあらず」「あらむ」「あるべし」など断定+「あり」の構文で頻用。';
  }

  // 動詞 (敬語動詞・重要動詞)
  if (pos === '動詞') {
    if (baseForm && hintMap['hon:' + baseForm]) return hintMap['hon:' + baseForm];
    if (text.startsWith('思しめ')) return hintMap['hon:おぼしめす'];
    if (text === '思し' || text === '思さ' || text === '思す' || text === '思し嘆き') return hintMap['hon:おぼす'];
    if (text === '御覧じ' || text === '御覧ず' || text === '御覧じやら') return hintMap['hon:ごらんず'];
    if (text === '聞こしめし' || text === '聞こしめす') return hintMap['hon:きこしめす'];
    if (text === 'あそばし') return hintMap['hon:あそばす'];
    if (text === 'つかはさ' || text === 'つかはす') return hintMap['hon:つかはす'];
    if (text === '聞こえさせ' || text === '聞こえ') return hintMap['hon:きこゆ'];
    if (text === 'おはせ' || text === 'おはし' || text === 'おはしまし' || text === 'おはします' || text === 'おはしませ' || text === 'おはし着き' || text === 'おはしまし着き') return hintMap['hon:おはす'];
    // 重要動詞
    if (text === 'あへ' || baseForm === 'あふ') return hintMap['vb:あふ'];
    if (text === '出でき' || text === '出で来' || baseForm === 'いでく') return hintMap['vb:いでく'];
    if (text === 'すぐれ' || baseForm === 'すぐる') return hintMap['vb:すぐる'];
    if (baseForm === 'まねぶ') return hintMap['vb:まねぶ'];
    if (text === 'はえ' || text === 'はゆ' || baseForm === 'はゆ') return hintMap['vb:はゆ'];
    if (text === 'さる' && cType === 'ラ変') return hintMap['vb:さる'];
    if (text === 'なし' && cType === 'サ四') return hintMap['vb:なす'];
    if (text === '流さ' || text === '流れゆく') return hintMap['vb:ながす'];
    if (baseForm === 'よる') return hintMap['vb:より'];
    // 形容動詞 (ナリ・タリ)
    if (text === 'あはれに') return hintMap['word:あはれに'];
    if (text === 'あやにくに') return hintMap['word:あやにくに'];
    if (text === '希有なる') return '形容動詞「希有なり」連体形: 「珍しい・滅多にない」。「まことに希有なるや」=「実に珍しいことよ」と語り手が驚嘆を示す。';
    if (text === '遥かなれ') return '形容動詞「遥かなり」已然形: 「遠い・はるか彼方だ」。下に「ども」(逆接) が続く。';
    if (text === 'なだらかに') return '形容動詞「なだらかなり」連用形: 「滑らかだ・穏やかだ」。世継の語り口を讃える。';
    if (text === 'ことのほかに') return '形容動詞「ことのほかなり」連用形: 「格別に・他と比べてはるかに」。比較・対照の修飾で本作の対比表現の核。';
    if (text === 'むげに') return hintMap['adv:むげに'];
    if (text === 'あやに') return '形容動詞「あやなり」連用形 (副詞用法): 「むやみに・並々ならず」。「目もあやに」=「目が眩むほど・驚き呆れるほど」。';
    // 変格・カ変・ナ変・サ変・ラ変
    if (cType === 'カ変') return 'カ変動詞「来 (く)」系: 「来る・出てくる」。本作では「出で来 (いでく)」が頻出。活用は「こ・き・く・くる・くれ・こ(命)」の特殊形。';
    if (cType === 'サ変') return 'サ変動詞「す」系: 「する」(本動詞)・複合動詞語幹 (例: 出家す・罪す・外目す)。活用は「せ・し・す・する・すれ・せよ」。';
    if (cType === 'ラ変') return 'ラ変動詞「あり」系: 「ある・いる・〜だ」。終止形が「-り」(あり・をり・侍り・いますがり) で特殊。連体形「ある」、已然形「あれ」。';
    // 一般動詞: 活用情報を提示
    if (cType && cForm) {
      const formName = { '未': '未然形', '用': '連用形', '終': '終止形', '体': '連体形', '已': '已然形', '命': '命令形' }[cForm] || cForm;
      const prettyType = cType.replace('ハ四', 'ハ行四段').replace('カ四', 'カ行四段').replace('サ四', 'サ行四段').replace('タ四', 'タ行四段').replace('マ四', 'マ行四段').replace('ラ四', 'ラ行四段').replace('バ四', 'バ行四段').replace('ナ四', 'ナ行四段').replace('ガ四', 'ガ行四段');
      // 重要語のみ抜粋
      if (['行ふ','下す','劣り','婿とり','つく','許す','吹く','おこす','忘る','とどむ','住む','隠る','いふ','作る','立つ','さる','浮く','漂ふ','別る','見る','頼む','たたふ','照らす','まねぶ','知り','おはす','かため','あり','聞こしめす'].some(b => baseForm === b || text.startsWith(b))) {
        return `動詞${prettyType ? '「' + prettyType + '」' : ''}${formName}: 基本形「${baseForm || text}」。`;
      }
    }
  }

  // 形容詞: 重要古語 + 標準
  if (pos === '形容詞') {
    if (text.startsWith('あはれ')) return hintMap['word:あはれに'];
    if (text === 'いみじく' || text === 'いみじ' || baseForm === 'いみじ') return hintMap['word:いみじく'];
    if (text === 'かしこく' || text === 'かしこし' || baseForm === 'かしこし') return hintMap['word:かしこく'];
    if (text === 'めでたく' || text === 'めでたし' || baseForm === 'めでたし') return hintMap['word:めでたく'];
    if (text === 'やすから') return hintMap['word:やすからず'];
    if (text === 'あさましく' || baseForm === 'あさまし') return hintMap['word:あさましく'];
    if (text === 'なき' || text === 'なし' || baseForm === 'なし' || baseForm === 'なく') return '形容詞「なし」(ク活): 「ない」。「なきこと」=「無実の罪」(讒言の婉曲)、「あるじなしとて」=「主人がいないからといって」。';
    if (text === 'よから' || baseForm === 'よし') return '形容詞「よし」(ク活) 未然形「よから」: 「良い・善い」。下に「ず」「ぬ」が続いて「よからぬこと」=「悪いこと」(讒言の婉曲)。';
    if (text === '若く' || baseForm === '若し') return '形容詞「若し」(ク活) 連用形: 「年が若い」。本作では帝・時平の若年を示し、対比される道真の老練と重要な対照をなす。';
    if (text === '幼く') return '形容詞「幼し」(ク活) 連用形: 「年が幼い」。「若し」より下の年齢を示す。「幼くおはしける男君・女君」が配流の哀感を強める。';
    if (text === '小さき') return '形容詞「小さし」(ク活) 連体形: 「小さい・幼い」。「小さきはあへなむ」=「幼い子は (流罪を) 免れてもよかろう」。';
    if (text === '悲しき' || text === '悲し' || text === '悲しく') return '形容詞「悲し」(シク活): 「悲しい・心が痛む」。本作は道真とその家族の悲劇を「悲し」で繰り返し強調。';
    if (text === '同じ') return '形容詞「同じ」(シク活 — 連体形は「同じ」「同じき」両形): 「同じ・同様の」。シク活用だが連体形が特殊で「同じき」のほか「同じ」もとる。';
    if (text === '遠く') return '形容詞「遠し」(ク活) 連用形: 「遠い」。「都遠くなるままに」=「都が遠ざかるにつれて」。距離の進行を示す。';
    if (text === '近く' || text === 'いと近く') return '形容詞「近し」(ク活) 連用形: 「近い」。「いと近く」=「たいそう近く」。';
    if (text === '心細く') return '形容詞「心細し」(ク活) 連用形: 「心細い・頼りない」。配流地での孤独感を示す重要心情語。';
    if (text === '明かき') return '形容詞「明かし」(ク活) 連体形: 「明るい」。「月の明かき夜」=「月が明るい夜」。';
    if (text === '清き') return '形容詞「清し」(ク活) 連体形: 「清い・潔白だ」。「清き心」=「潔白な心」(無実の主張)。';
    if (text === 'おどろおどろしき') return '形容詞「おどろおどろし」(シク活) 連体形: 「大げさだ・仰々しい」。世継の語る話の衝撃性を示す。';
    if (text === 'ゆゑゆゑしう') return '形容詞「ゆゑゆゑし」(シク活) 連用形 (ウ音便): 「由緒ありげだ・趣深い」。世継の語りの教養深さを讃える。';
  }

  // 副詞
  if (pos === '副詞') {
    if (hintMap['adv:' + text]) return hintMap['adv:' + text];
    if (text === '皆') return '副詞「皆」: 「すべて・ことごとく」。総括の副詞。';
    if (text === '世に') return '副詞「世に」: 「世間で・並々ならず」(肯定)、「決して〜ない」(打消下)。本作では「才世にすぐれ」=「学識が並々ならず優れている」。';
    if (text === 'かたがたに') return '副詞「かたがたに」: 「あちらこちらに・別々の方向に」。子供たちが各方面に流された悲劇を示す。';
    if (text === 'ゆくゆくと') return '副詞「ゆくゆくと」: 「歩みを進めるにつれて・進みながら」。旅の進行と心情の変化を結ぶ。';
    if (text === 'ことのほかに') return '副詞「ことのほかに」: 「格別に・他と比べてはるかに」。比較・対照の修飾。';
  }

  // 係助詞
  if (pos === '係助詞') return hintMap[text];
  // 副助詞
  if (pos === '副助詞') return hintMap[text];
  // 終助詞
  if (pos === '終助詞') {
    if (text === 'な') return hintMap['na-禁止'];
    return hintMap[text];
  }
  // 間投助詞
  if (pos === '間投助詞') {
    if (text === 'や') return '間投助詞「や」: 詠嘆・並列「〜よ・〜と」。「歌や詩などを」=「歌や詩などを」(並列)、文末では詠嘆「〜だなあ」。';
  }
  // 接続助詞
  if (pos === '接続助詞') {
    if (text === 'て') return hintMap['conj:て'];
    if (text === 'ば') return hintMap['conj:ば'];
    if (text === 'ども') return hintMap['conj:ども'];
    if (text === 'ど') return hintMap['conj:ど'];
    if (text === 'つつ') return hintMap['conj:つつ'];
    if (text === 'を') return hintMap['conj:を'];
    if (text === 'に') return hintMap['conj:に'];
  }
  // 接続詞
  if (pos === '接続詞') {
    if (text === 'また') return hintMap['adv:また'];
    if (text === 'かくて') return '接続詞「かくて」: 「こうして・そして」。場面転換・章立ての切れ目を示す。「かく+て」が縮約した語形。';
  }
  // 代名詞: 重要なものだけ
  if (pos === '代名詞') {
    if (text === 'これ' || text === 'こ') return '代名詞「これ・こ」(近称): 「これ・この」。指示対象は直前で述べた事物。';
    if (text === 'それ' || text === 'そ') return '代名詞「それ・そ」(中称): 「それ・その」。聞き手の領域を指す指示語で、本作では子どもたちの一群を指す。';
    if (text === 'われ') return '代名詞「われ」: 「私・自分」。自称代名詞。本作では道真の和歌の一人称。';
    if (text === '君') return '代名詞「君」: 「あなた・きみ」。二人称敬称。本作では道真が宇多上皇または醍醐天皇を指す。';
  }
  // 命令形 (動詞「べき」など)
  if (pos === '命令') {
    if (text === 'べき') return hintMap['べき'];
  }

  return null;
}

let added = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    if (tk.hint) continue;
    const hint = pickHint(tk);
    if (hint) {
      tk.hint = hint;
      added++;
    }
  }
}

// 検証
let allOk = true;
for (const s of t.sentences) {
  const concat = s.tokens.map(tk => tk.text).join('');
  if (concat !== s.originalText) { allOk = false; console.error('MISMATCH ' + s.id); }
}
if (!allOk) { console.error('Validation failed.'); process.exit(1); }

const tokens = t.sentences.flatMap(s => s.tokens);
const meaningful = tokens.filter(tk => tk.layer > 0 || tk.grammarTag?.pos);
const wh = meaningful.filter(tk => tk.hint).length;
const allHints = tokens.filter(tk => tk.hint).length;

fs.writeFileSync(fp, JSON.stringify(t, null, 2), 'utf8');
if (fs.existsSync(path.dirname(distFp))) {
  fs.writeFileSync(distFp, JSON.stringify(t, null, 2), 'utf8');
}

console.log(`Added ${added} hints. Total hints: ${allHints}, meaningful coverage: ${wh}/${meaningful.length} = ${Math.round(wh / meaningful.length * 100)}%`);
