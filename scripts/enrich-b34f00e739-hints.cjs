setAux('vb:あへなむ', '慣用「あへなむ」: 動詞「あふ」(ハ下二「敢ふ・耐ふ」) 連用形 +「な」(完了・強意 未然) +「む」(推量)。「差し支えないだろう・我慢できるだろう」。');
