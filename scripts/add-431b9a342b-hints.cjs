// hint 辞書: テキスト/品詞/baseForm/conjugationForm の組み合わせで一意決定
// key の優先度: text+baseForm+form > text+baseForm > text+pos > text
function makeKey(tk) {
  const text = tk.text;
  const pos = tk.grammarTag?.pos || '';
  const base = tk.grammarTag?.baseForm || '';
  const form = tk.grammarTag?.conjugationForm || '';
  return [text, pos, base, form];
}

function lookup(tk, dict) {
  const [text, pos, base, form] = makeKey(tk);
  // 完全一致 (text + base + form)
  if (form && dict[`${text}|${base}|${form}`]) return dict[`${text}|${base}|${form}`];
  if (dict[`${text}|${base}`]) return dict[`${text}|${base}`];
  if (pos && dict[`${text}|${pos}`]) return dict[`${text}|${pos}`];
  if (dict[text]) return dict[text];
  return null;
}

