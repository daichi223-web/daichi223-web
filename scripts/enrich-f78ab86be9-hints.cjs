let added = 0;
let skipped = 0;
const unmatched = [];

for (const s of t.sentences) {
  for (const tk of s.tokens) {
    if (tk.hint) { skipped++; continue; }
    const meaningful = tk.layer > 0 || (tk.grammarTag && tk.grammarTag.pos);
    if (!meaningful) continue;

    // 1. ID override first
    if (ID_OVERRIDES[tk.id]) {
      tk.hint = ID_OVERRIDES[tk.id];
      added++;
      continue;
    }

    // 2. Pattern match (most specific to least)
    const pos = tk.grammarTag?.pos || '';
    const base = tk.grammarTag?.baseForm || '';
    const meaning = tk.grammarTag?.meaning || '';
    const conj = tk.grammarTag?.conjugationType || '';
    const form = tk.grammarTag?.conjugationForm || '';
    const hon = tk.grammarTag?.honorific || '';

    const keys = [
      // Ordered most specific → least specific
      `${tk.text}|${pos}|${base}|${meaning}|${conj}|${form}|${hon}`,
      `${tk.text}|${pos}|${base}|${meaning}|${conj}|${form}`,
      `${tk.text}|${pos}||${meaning}|${conj}|${form}|${hon}`,
      `${tk.text}|${pos}||${meaning}|${conj}|${form}`,
      `${tk.text}|${pos}|${base}|${meaning}||||`,
      `${tk.text}|${pos}|${base}|${meaning}||`,
      `${tk.text}|${pos}|${base}|${meaning}`,
      `${tk.text}|${pos}||${meaning}|||`,
      `${tk.text}|${pos}||${meaning}`,
      `${tk.text}|${pos}|${base}||${conj}|${form}|${hon}`,
      `${tk.text}|${pos}|${base}||${conj}|${form}`,
      `${tk.text}|${pos}|${base}||${conj}`,
      `${tk.text}|${pos}|${base}|`,
      `${tk.text}|${pos}|${base}`,
      `${tk.text}|${pos}|||${conj}|${form}|${hon}`,
      `${tk.text}|${pos}|||${conj}|${form}`,
      `${tk.text}|${pos}|||${conj}`,
      `${tk.text}|${pos}||`,
      `${tk.text}|${pos}`,
      `${tk.text}`,
    ];

    let hit = null;
    for (const k of keys) {
      if (PATTERNS[k]) { hit = PATTERNS[k]; break; }
    }
    if (hit) {
      tk.hint = hit;
      added++;
    } else {
      unmatched.push({ id: tk.id, text: tk.text, pos, base, meaning, conj, form, hon });
    }
  }
}

