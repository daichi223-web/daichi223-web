// ── Step 4: 重複文除去後の sentences を順次再番号 + token id 振り直し
//                + hint 適用 ───────────────────────────
const newSentences = [];
let oldToNewId = {};
for (let i = 0; i < kept.length; i++) {
  const s = kept[i];
  const newId = 's' + (i + 1);
  oldToNewId[s.id] = newId;
  // tokens の id も新しい sentence id でリネーム
  const newTokens = s.tokens.map((tk, j) => {
    const newTokenId = `${newId}-t${j + 1}`;
    const lookupKey = `${s.id}:${tk.id}`;  // 旧 sid:旧 tid で hint テーブルを引く
    const out = { ...tk, id: newTokenId };
    if (!out.hint && hintTable[lookupKey]) {
      out.hint = hintTable[lookupKey];
    }
    return out;
  });
  // start/end を再採番
  let pos = 0;
  for (const tk of newTokens) {
    tk.start = pos;
    tk.end = pos + tk.text.length;
    pos += tk.text.length;
  }
  newSentences.push({
    id: newId,
    originalText: newTokens.map(tk => tk.text).join(''),
    modernTranslation: s.modernTranslation,
    tokens: newTokens,
  });
}
t.sentences = newSentences;

// ── 検証 + 書き出し ────────────────────────────────────
let ok = true;
for (const s of t.sentences) {
  const concat = s.tokens.map(tk => tk.text).join('');
  if (concat !== s.originalText) { ok = false; console.log('MISMATCH', s.id); }
}
console.log('alignment ok:', ok);
console.log('sentences:', t.sentences.length);
const allTokens = t.sentences.flatMap(s => s.tokens);
console.log('tokens:', allTokens.length);
const meaningful = allTokens.filter(tk => (tk.layer && tk.layer > 0) || (tk.grammarTag && tk.grammarTag.pos));
const wh = meaningful.filter(tk => tk.hint).length;
console.log('hint coverage on meaningful:', wh, '/', meaningful.length, '=', Math.round(wh / meaningful.length * 100) + '%');
console.log('total hints:', allTokens.filter(tk => tk.hint).length);

// 書き出し
fs.writeFileSync(fp, JSON.stringify(t, null, 2), 'utf8');
console.log('wrote:', fp);

