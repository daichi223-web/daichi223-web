// 既存 hint をチェック (上書きしない・重複検出)
let added = 0;
let skippedExisting = 0;
let notFound = 0;
const notFoundIds = [];
for (const [tokenId, hint] of Object.entries(hints)) {
  const [sid] = tokenId.split('-');
  const s = t.sentences.find(x => x.id === sid);
  if (!s) {
    notFound++;
    notFoundIds.push(tokenId);
    continue;
  }
  const tk = s.tokens.find(x => x.id === tokenId);
  if (!tk) {
    notFound++;
    notFoundIds.push(tokenId);
    continue;
  }
  if (tk.hint) {
    skippedExisting++;
    continue;
  }
  tk.hint = hint;
  added++;
}

if (notFound > 0) {
  console.error(`Warning: ${notFound} token IDs not found:`);
  for (const id of notFoundIds) console.error('  -', id);
}

