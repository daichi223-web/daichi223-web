// Apply hints
let added = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    const key = tk.id;
    if (hints[key] && !tk.hint) {
      tk.hint = hints[key];
      added++;
    }
  }
}

fs.writeFileSync(targetPath, JSON.stringify(t, null, 2), 'utf8');

// Coverage check
let total = 0, withHint = 0, meaningful = 0, meaningfulWithHint = 0;
for (const s of t.sentences) {
  for (const tk of s.tokens) {
    total++;
    if (tk.hint) withHint++;
    if (tk.layer > 0 || tk.grammarTag?.pos) {
      meaningful++;
      if (tk.hint) meaningfulWithHint++;
    }
  }
}
console.log(`hints added: ${added}`);
console.log(`total tokens: ${total}, with hint: ${withHint}`);
console.log(`meaningful: ${meaningful}, with hint: ${meaningfulWithHint}`);
console.log(`coverage: ${Math.round(meaningfulWithHint/meaningful*100)}%`);

// Alignment check
let ok = true;
for (const s of t.sentences) {
  if (s.tokens.map(tk => tk.text).join('') !== s.originalText) {
    ok = false;
    console.log('MISMATCH', s.id);
  }
}
console.log('alignment ok:', ok);
