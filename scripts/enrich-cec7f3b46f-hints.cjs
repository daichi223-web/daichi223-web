#!/usr/bin/env node
// Enrich token.hint for kobun-tan 教材「光源氏の誕生」(cec7f3b46f)
// Strategy: per-token rule based on text + grammarTag (pos/baseForm/meaning/conjugationForm)

const fs = require('fs');
const path = require('path');

const SRC = path.resolve(__dirname, '../public/texts-v3/cec7f3b46f.json');
const data = JSON.parse(fs.readFileSync(SRC, 'utf8'));

// Helper: build hint by token text + grammar tag context
function buildHint(tk) {
  if (tk.hint) return null; // keep existing
  const g = tk.grammarTag || {};
  const text = tk.text;
  const pos = g.pos || '';
  const base = g.baseForm || '';
  const meaning = g.meaning || '';
  const form = g.conjugationForm || '';
  const ctype = g.conjugationType || '';
  const honor = g.honorific || '';

  // ===== 助動詞 =====
  if (pos === '助動詞') {
    if (text === 'けり') return '助動詞「けり」: 過去 (〜た) ＋ 詠嘆 (〜だなあ)。連用形接続。物語の地の文では伝聞過去、和歌では詠嘆が基本';
    if (text === 'ける' || text === 'けれ') return '助動詞「けり」 (' + form + '形): 過去・詠嘆。連用形接続。物語の地の文で多用される基幹助動詞';
    if (text === 'き') return '助動詞「き」: 直接過去 (〜た)。話し手が直接体験した過去。連用形接続。「けり」(伝聞過去) と対比';
    if (text === 'し' && meaning === '過去') return '助動詞「き」連体形「し」: 直接体験の過去 (〜た)。連用形接続。「けり」(伝聞過去) との識別が頻出';
    if (text === 'けむ') return '助動詞「けむ」: 過去推量 (〜ただろう) ／ 過去原因推量 (〜たのだろう)。連用形接続。「にやありけむ」は典型的余情表現';
    if (text === 'らむ') return '助動詞「らむ」: 現在推量 (今ごろ〜だろう) ／ 現在原因推量。終止形接続 (ラ変は連体形)';
    if (text === 'む' && meaning === '婉曲') return '助動詞「む」: ここは婉曲 (〜ような)。未然形接続。連体修飾の位置では婉曲・仮定の用法に';
    if (text === 'む') return '助動詞「む」: 推量・意志 (〜だろう・〜よう) ／ 連体修飾位置では婉曲・仮定。未然形接続';
    if (text === 'べき' || text === 'べく' || text === 'べし' || text === 'べかり') {
      if (meaning === '可能') return '助動詞「べし」 (' + form + '形): ここは可能 (〜できる)。打消「ず」を伴って「えーず」と並ぶ強い不可能表現になることも';
      if (meaning === '当然') return '助動詞「べし」 (' + form + '形): ここは当然・義務 (〜はずだ・〜ねばならぬ)。終止形接続 (ラ変は連体形)';
      return '助動詞「べし」 (' + form + '形): 推量・当然・意志・可能・命令・適当の六義。終止形接続 (ラ変は連体形)。文脈判別が頻出';
    }
    if (text === 'ぬ' && meaning === '打消') return '助動詞「ず」連体形「ぬ」: 打消 (〜ない)。未然形接続。終止形「ず」・連体形「ぬ」・已然形「ね」の識別が要';
    if (text === 'ず' || text === 'ざり' || text === 'ざる' || text === 'ね') return '助動詞「ず」 (' + form + '形): 打消 (〜ない)。未然形接続。「ず／ぬ／ね」と「ざら／ざり／ざる／ざれ」の二系列を持つ';
    if (text === 'ぬ' && (meaning === '完了' || meaning === '強意')) return '助動詞「ぬ」 (' + form + '形): 完了 (〜た) ／ 推量を伴うと強意「きっと〜」。連用形接続。「ぬべし／なむ」は強意の典型';
    if (text === 'つ' && meaning === '強意') return '助動詞「つ」: ここは強意 (きっと〜)。後接「べし／む」とで「つべし／つらむ」など強い推量を作る';
    if (text === 'つ' || text === 'て' || text === 'つる' || text === 'つれ') return '助動詞「つ」 (' + form + '形): 完了 (〜た) ／ 推量と結びつき強意。連用形接続';
    if (text === 'たり' || text === 'たる' || text === 'たれ') {
      if (meaning === '断定') return '助動詞「たり」 (' + form + '形): 断定 (〜である)。体言＋たり、漢文訓読系。「なり」より硬めの語感';
      return '助動詞「たり」 (' + form + '形): 完了 (〜た) ／ 存続 (〜ている)。連用形接続。動作の完了点を含意';
    }
    if (text === 'り' || text === 'る' || text === 'れ') {
      if (meaning === '存続' || meaning === '完了') return '助動詞「り」 (' + form + '形): 存続 (〜ている) ／ 完了 (〜た)。サ変未然形・四段已然形に接続。「サミシイリ」と覚える';
    }
    if (text === 'なり' && meaning === '断定') return '助動詞「なり」: 断定 (〜である)。体言＋なり、活用語連体形＋なり。「に」(連用) ＋係助詞の形で頻出';
    if (text === 'に' && meaning === '断定') return '助動詞「なり」連用形「に」: 断定 (〜で／〜であって)。「にや」「にこそ」「にて」の核で、後ろに「あり」を補って読む';
    if (text === 'なら' || text === 'なる' || text === 'なれ') return '助動詞「なり」 (' + form + '形): 断定 (〜である)。体言・連体形に接続。終止形接続なら伝聞推定の「なり」になる';
    if (text === 'な' && meaning === '断定') return '助動詞「なり」連体形「な」: 断定。「なめり」「なり」(伝聞) などと識別。「なめり」=「なるめり」音便で推定';
    if (text === 'めり') return '助動詞「めり」: 視覚推定 (〜のようだ・〜と見える)。終止形接続 (ラ変は連体形)。「なめり」=断定「な」+「めり」';
    if (text === 'らし') return '助動詞「らし」: 根拠ある推定 (〜らしい)。終止形接続。「らむ」より客観的な推定';
    if (text === 'まじ') return '助動詞「まじ」: 「べし」の打消 (〜ないだろう・〜まい)。終止形接続。打消推量・打消意志など';
    if (text === 'させ' && meaning === '尊敬') return '助動詞「さす」連用形「させ」: ここは尊敬 (〜なさる)。「させたまふ」は二重敬語で最高敬語に';
    if (text === 'せ' && meaning === '尊敬') return '助動詞「す」連用形「せ」: ここは尊敬 (〜なさる)。「せたまふ」は二重敬語で帝など最高位の人物に用いる';
    if (text === 'せ' && meaning === '使役') return '助動詞「す」連用形「せ」: 使役 (〜させる)。下に尊敬語を伴わない時は使役、伴うと尊敬になりやすい';
    return '助動詞「' + text + '」 (' + (meaning || form) + '): ' + (meaning ? '意味=' + meaning : '') + '。文中の働きと接続を確認';
  }

  // ===== 係助詞・副助詞 =====
  if (pos === '係助詞') {
    if (text === 'ぞ') return '係助詞「ぞ」: 強意。結びは連体形 (係り結び)。訳出不要なことが多いが、強い指示・特定を含意';
    if (text === 'なむ') return '係助詞「なむ」: 強意 (〜こそ)。結びは連体形 (係り結び)。「なむ」終助詞 (願望) と識別注意';
    if (text === 'や') return '係助詞「や」: 疑問・反語。結びは連体形。文末の「や」は詠嘆や疑問の終助詞になることも';
    if (text === 'か') return '係助詞「か」: 疑問・反語。結びは連体形 (係り結び)。「いづれの〜にか」は反語的余情の含み';
    if (text === 'こそ') return '係助詞「こそ」: 強意。結びは已然形 (係り結び)。「こそあれ」「〜こそすれ」など中止逆接の用法も頻出';
    if (text === 'は' && meaning === '提示') return '係助詞「は」: 提示・主題。他と区別する含み。「は」が複数回出ると対比の色合いが強まる';
    if (text === 'は' && meaning === '順仮') return '係助詞「は」: 仮定条件 (〜ならば) を作る。「未然形＋ば」の「ば」と類似だが「ようせずは」のような形で頻出';
    if (text === 'は') return '係助詞「は」: 主題・対比の提示。格助詞「を／に」などを抑え主題を浮かせる';
    if (text === 'も') return '係助詞「も」: 並列・添加・強意。「〜も」を重ねれば並列、単独は強意・類似';
    return '係助詞「' + text + '」: 結びの形 (連体／已然) を確認。係り結びは古文読解の基幹';
  }
  if (pos === '副助詞') {
    if (text === 'のみ') return '副助詞「のみ」: 限定 (〜だけ・〜ばかり)。強意のニュアンスを帯びることも。「ばかり」よりやや書き言葉的';
    if (text === 'だに') return '副助詞「だに」: 類推 (〜さえ) ／ 最小限 (せめて〜だけでも)。下に願望・命令を伴うと最小限';
    if (text === 'さへ') return '副助詞「さへ」: 添加 (〜までも)。すでにある事柄に加えて。「だに」(類推) と区別';
    if (text === 'すら') return '副助詞「すら」: 類推 (〜さえ)。「だに」とほぼ同義だが古めの語感';
    if (text === 'など') return '副助詞「など」: 例示・婉曲・引用。「〜など (と)」の形で発話・思考を婉曲化する用法も';
    if (text === 'ばかり') return '副助詞「ばかり」: 限定 (〜だけ) ／ 程度 (〜ほど)。文脈で見分ける';
    if (text === 'まで') return '副助詞「まで」: 範囲・程度。「〜にまで及ぶ」のニュアンス';
    if (text === 'し') return '副助詞「し」: 強意。「しも」「しぞ」のように下接して強める。識別語として頻出';
    return '副助詞「' + text + '」: 限定・添加・類推など。意味の見分けが頻出';
  }

  // ===== 補助動詞 (敬語) =====
  if (pos === '補助動詞') {
    if (base === 'たまふ' || text.startsWith('たま')) {
      return '尊敬の補助動詞「たまふ」: 〜なさる・お〜になる。動作主への敬意。ハ行四段。下二段の「たまふ」(謙譲・自分側) と識別';
    }
    if (base === 'おはす' || text.startsWith('おはし') || text === 'おはす') {
      return '尊敬の補助動詞「おはす」: 〜ていらっしゃる。サ変。「あり」の尊敬。動作主への高い敬意';
    }
    if (base === 'きこゆ' || text.includes('きこゆ') || text.startsWith('きこえ')) {
      return '謙譲の補助動詞「きこゆ」: お〜申し上げる。動作の受け手への敬意。ヤ行下二段';
    }
    if (base === 'たてまつる' || text.includes('たてまつ')) {
      return '謙譲の補助動詞「たてまつる」: お〜申し上げる・〜してさしあげる。動作の受け手への敬意';
    }
    if (base === 'さぶらふ' || base === 'はべり') {
      return '丁寧の補助動詞「さぶらふ／はべり」: 〜ます・〜です。聞き手 (読者) への敬意';
    }
    if (base === 'あり' || text === 'あり' || text === 'あら' || text === 'あれ' || text === 'ある') {
      return '補助動詞「あり」: 「〜にあり」「〜てあり」の形で状態・存続を示す。ラ変。断定「なり」の核';
    }
    return '補助動詞「' + (base || text) + '」: 本動詞ではなく状態・敬意などを補う働き';
  }

  // ===== 形容動詞 (pos==動詞 + ctype==ナリ になっている場合あり) =====
  if (ctype === 'ナリ' || pos === '形容動詞') {
    if (text.startsWith('あはれ')) return '重要語「あはれなり」: しみじみと心打たれる・気の毒だ・愛しい。古文最重要情緒語。喜怒哀楽すべての強い感慨を含む';
    if (text.startsWith('きよら')) return '重要語「きよらなり」: 気高く美しい・清らかで美しい。最上級の美の表現で、皇族・貴人の美貌に用いる';
    if (text.startsWith('めづらか')) return '重要語「めづらかなり」: 並々ならず素晴らしい・珍しい。「めづらし」の形容動詞形で賛美の含み';
    if (text.startsWith('心細げ')) return '形容動詞「心細げなり」: 心細そうな様子だ。「心細し」＋接尾「げ」(様子)。形容詞語幹＋げなり型';
    if (text.startsWith('里がち')) return '形容動詞「里がちなり」: 里 (実家) に下がりがちだ。「がち」(その傾向が多い) ＋「なり」。後宮を退出することが多い状態';
    if (text.startsWith('華やか')) return '形容動詞「華やかなり」: 華やかだ・はなばなしい。「世のおぼえ華やか」=世間の評判が華々しい';
    if (text.startsWith('あながち')) return '重要語「あながちなり」: 強引だ・むやみだ・しいて。下に打消を伴うと「あながちならず」=必ずしも〜ではない';
    if (text.startsWith('心こと')) return '形容動詞「心ことなり」: 特別だ・格別だ。「心こと」=心づかいが格別、の意';
    if (text.startsWith('なかなか')) return '重要語「なかなかなり」: かえって〜だ・中途半端だ。「なかなかなるもの思ひ」=かえって苦しい物思い';
    return '形容動詞 (ナリ活用)「' + text + '」: 「〜なり／〜なる／〜なれ」と活用。性質・状態を表す';
  }

  // ===== 動詞 =====
  if (pos === '動詞') {
    // 敬語動詞
    if (base === 'さぶらふ' || text === 'さぶらひ' || text === 'さぶらは' || text === 'さぶらふ') {
      return '謙譲動詞「さぶらふ」: お仕え申し上げる・お側に控える。後宮の女官・従者の動作。「侍り」とほぼ同義の謙譲・丁寧';
    }
    if (base === 'おはします' || text.startsWith('おはしませ') || text.startsWith('おはします')) {
      return '尊敬動詞「おはします」: いらっしゃる・おいでになる。「おはす」より敬意が高い最高敬語の一つ';
    }
    if (base === 'おはす' || text === 'おはす' || text === 'おはせ') {
      return '尊敬動詞「おはす」: いらっしゃる。「あり／行く／来」の尊敬。サ変。動作主への敬意';
    }
    if (text === '御覧ずる' || text === '御覧じ' || base === '御覧ず') {
      return '尊敬動詞「御覧ず」: ご覧になる。「見る」の尊敬。サ変。帝・貴人の動作に用いる';
    }
    if (text === 'まう上ら' || text === '参う上り') {
      return '謙譲動詞「まうのぼる／参う上る」: 参上する・お上がりする。「のぼる」の謙譲。御所・帝のもとへ参上する動作';
    }
    if (text === '参ら' || text === '参り' || base === '参る' || base === 'まゐる') {
      return '謙譲動詞「参る」: 参上する・差し上げる。「行く・来」「与ふ」の謙譲。動作の受け手 (帝など) への敬意';
    }
    if (text === '思ほし' || base === '思ほす') {
      return '尊敬動詞「思ほす」: お思いになる。「思ふ」の尊敬。「おぼす」とほぼ同じで帝・貴人の心情に用いる';
    }
    if (text === 'おぼし' || text === 'おぼす' || base === 'おぼす') {
      return '尊敬動詞「おぼす」: お思いになる。「思ふ」の尊敬。サ四。帝・貴人の心の働きに用いる';
    }
    if (text === '賜はす' || base === '賜はす') {
      return '尊敬動詞「賜はす」: お与えになる。「与ふ」の尊敬。サ下二。動作主 (帝) への敬意';
    }
    // ラ変
    if (ctype === 'ラ変') return 'ラ変動詞「' + (base || text) + '」: ら／り／り／る／れ／れ。「あり・をり・はべり・いまそかり」の四語が代表';
    if (ctype === 'サ変') return 'サ変動詞「' + (base || text) + '」: せ／し／す／する／すれ／せよ。「す・おはす」など。「〜す」型の語尾語が多数';
    if (ctype === 'カ変') return 'カ変動詞「来 (く)」: こ／き／く／くる／くれ／こ (よ)。唯一「来」一語のみ';
    if (ctype === 'ナ変') return 'ナ変動詞「' + (base || text) + '」: な／に／ぬ／ぬる／ぬれ／ね。「死ぬ・往ぬ」の二語のみ';
    return null; // 一般動詞は省略
  }

  // ===== 形容詞・形容動詞 =====
  if (pos === '形容詞') {
    // 重要古今異義語
    if (text.startsWith('やむごとな')) return '形容詞「やむごとなし」: 高貴だ・尊い。家柄や身分が抜きん出ているさま。後宮では女御・更衣の家格を語る基本語';
    if (text.startsWith('めざまし')) return '重要語「めざまし」: 心外だ・気に食わない・目に余る。現代語「目覚ましい」(立派だ) と意味が逆。後宮の嫉妬・反感の表現で頻出';
    if (text.startsWith('あぢきな')) return '重要語「あぢきなし」: どうしようもない・つまらない・不愉快だ。意のままにならない事態への当惑・不満';
    if (text.startsWith('はしたな')) return '重要語「はしたなし」: きまりが悪い・中途半端で不快だ。現代語「はしたない」(下品) と意味が違う';
    if (text.startsWith('かたじけな')) return '重要語「かたじけなし」: もったいない・恐れ多い・ありがたい。神仏や貴人の恩恵への謙譲表現';
    if (text.startsWith('たぐひな')) return '重要語「たぐひなし」: 比べるものがない・並外れている。最上級の賛美';
    if (text.startsWith('はかばかし')) return '重要語「はかばかし」: しっかりしている・頼りになる。打消を伴い「はかばかしからず」=頼りない、で頻出';
    if (text === 'よう' || text === 'ようせず' || text.startsWith('ようせ')) return '副詞句「ようせずは」: ともすると・ひょっとすると。「ようす」(うまくやる) ＋打消＋仮定で「下手をすれば」のニュアンス';
    if (text.startsWith('まばゆ')) return '重要語「まばゆし」: まぶしい・気恥ずかしい・見ていられない。光に対する眩しさと、目の置き場のなさ・気後れの両義';
    if (text.startsWith('あいな')) return '重要語「あいなし」: 気に食わない・つまらない・なんとなく。情緒的不快や無関心を示す';
    if (text.startsWith('まさな')) return '重要語「まさなし」: よろしくない・不都合だ・見苦しい。「正無し」の意で品格を欠くさま';
    if (text.startsWith('わづらはし')) return '重要語「わづらはし」: 気がかりだ・厄介だ・面倒だ。心理的負担を伴う事態に用いる';
    if (text.startsWith('わりな')) return '重要語「わりなし」: 道理に合わない・どうしようもない・むやみだ。「理 (ことわり) 無し」の意';
    if (text.startsWith('かしこ')) return '重要語「かしこし」: 恐れ多い・畏れ多い・優れている。神仏・帝など高位への畏怖と尊敬を示す';
    if (text.startsWith('心苦し')) return '重要語「心苦し」: 気の毒だ・つらい・苦痛だ。他者への同情と、自分自身の苦痛の両義';
    if (text.startsWith('あやし')) return '重要語「あやし」: 不思議だ・身分が低い・粗末だ。「奇し／賤し」両方の意があり文脈で見分け';
    if (text.startsWith('あつし')) return '重要語「あつし」: 病気がちだ・病弱だ。「篤し」と書く。「あつしくなりゆく」=病気がちになっていく';
    if (text.startsWith('上衆めか')) return '重要語「上衆めかし」: 上品だ・高貴な感じがする。「上衆」(身分の高い人) ＋「めかし」(〜らしい)';
    if (text.startsWith('たへがた')) return '重要語「たへがたし」: 我慢できない・耐えられない。物理・心理両面の限界を表す';
    if (text === 'なし' || text === 'なき' || text === 'なく' || text === 'なけれ' || text === 'なかれ') return '形容詞「なし」: 「無し」。ク活用。連体形「なき」連用形「なく」已然形「なけれ」が頻出';
    if (text.startsWith('限りな')) return '重要語「限りなし」: この上ない・最高だ。最上級表現。「〜こと限りなし」で文末の余情を作る';
    if (text.startsWith('深か')) return '形容詞「深し」: ク活用。「契り深し」=因縁が深い。「深かり」連用形＋「けむ」で「深かったのだろう」';
    return null;
  }

  if (pos === '形容動詞' || ctype === 'ナリ') {
    if (text.startsWith('あはれ')) return '重要語「あはれなり」: しみじみと心打たれる・気の毒だ・愛しい。古文最重要情緒語。喜怒哀楽すべての強い感慨を含む';
    if (text.startsWith('きよら')) return '重要語「きよらなり」: 気高く美しい・清らかで美しい。最上級の美の表現で、皇族・貴人の美貌に用いる';
    if (text.startsWith('めづらか')) return '重要語「めづらかなり」: 並々ならず素晴らしい・珍しい。「めづらし」の形容動詞形で賛美の含み';
    if (text.startsWith('心細げ')) return '形容動詞「心細げなり」: 心細そうな様子だ。「心細し」＋接尾「げ」(様子)。形容詞語幹＋げなり型';
    if (text.startsWith('里がち')) return '形容動詞「里がちなり」: 里 (実家) に下がりがちだ。「がち」(その傾向が多い) ＋「なり」。後宮を退出することが多い状態';
    if (text.startsWith('華やか')) return '形容動詞「華やかなり」: 華やかだ・はなばなしい。「世のおぼえ華やか」=世間の評判が華々しい';
    if (text.startsWith('あながち')) return '重要語「あながちなり」: 強引だ・むやみだ・しいて。下に打消を伴うと「あながちならず」=必ずしも〜ではない';
    if (text.startsWith('心こと')) return '形容動詞「心ことなり」: 特別だ・格別だ。「心こと」=心づかいが格別、の意';
    if (text.startsWith('なかなか')) return '重要語「なかなかなり」: かえって〜だ・中途半端だ。「なかなかなるもの思ひ」=かえって苦しい物思い';
    return null;
  }

  // ===== 副詞 =====
  if (pos === '副詞') {
    if (text === 'いと') return '副詞「いと」: 非常に・たいそう。古文最頻出の程度副詞。打消を伴うと「あまり〜ない」(部分否定)';
    if (text === 'いとど') return '重要副詞「いとど」: いっそう・ますます。「いと」の強調形で、状態の進行・累加を示す';
    if (text === 'あまた') return '重要語「あまた」: 数多く・たくさん。「数多」と書き、人や物が大量にあるさま';
    if (text === 'やうやう') return '副詞「やうやう」: しだいに・だんだんと。「やうやく」の古形。状態の漸進';
    if (text === 'いつしか') return '重要副詞「いつしか」: 早く〜たい・いつのまにか。願望表現と結ぶと「早く〜してほしい」';
    if (text === 'なほ') return '重要副詞「なほ」: やはり・依然として・さらに。基本義「やはり」を押さえる';
    if (text === 'まして') return '重要副詞「まして」: いっそう・なおさら。「まして〜ず」=なおさら〜ない';
    if (text === 'すぐれて') return '副詞「すぐれて」: 抜きん出て・特に。「優る」の連用＋接続助詞由来の副詞';
    if (text === 'まづ') return '副詞「まづ」: 真っ先に・まず。動作の優先順位を示す';
    if (text === 'え' || text === 'え ') return '重要副詞「え」: 下に打消・反語を伴って「〜できない」(不可能)。「えーず／えーじ」で頻出';
    if (text.startsWith('いよいよ')) return '副詞「いよいよ」: ますます・いっそう。状態の漸進を強める';
    if (text === 'おのづから') return '重要副詞「おのづから」: 自然と・ひとりでに・たまたま。意志を介さない発生・偶然性';
    if (text === 'げに') return '重要副詞「げに」: 本当に・なるほど。納得・同意を示す';
    return null;
  }

  // ===== その他 =====
  if (pos === '代名詞') {
    if (text === 'いづれ') return '疑問代名詞「いづれ」: どれ・どの・どちら。「いづれの御時にか」は『源氏物語』冒頭の名文句';
    if (text === 'われ') return '代名詞「われ」: 一人称。「われは」(自分こそは) の形で他者と対比し自負を示す';
    if (text === 'そ') return '代名詞「そ」: それ。「その」「それ」の語幹';
    return null;
  }

  // 格助詞「が」(同格) は重要なので説明
  if (pos === '格助詞') {
    if (text === 'が' && meaning === '同格') return '格助詞「が」(同格): 〜で・〜であって。「いとやむごとなききはにはあらぬが、すぐれて時めきたまふ」=「身分は高くない人で、寵愛を受けていた人」';
    if (text === 'の' && meaning === '同格') return '格助詞「の」(同格): 〜で・〜であって。連体修飾と紛らわしいが、後ろに同じ性質の体言が省略されている';
    if (text === 'の' && meaning === '主格') return '格助詞「の」(主格): 〜が。連体修飾節の中で主語を示す。「の」と「が」は古文では主格でも用いる';
    if (text === 'にて') return '格助詞「にて」: 場所・時・手段・原因。「に」+「て」由来。「〜において・〜で・〜によって」と訳し分ける';
    if (text === 'より' && meaning === '比較') return '格助詞「より」(比較): 〜より。比較の対象を示す。「〜には劣る／優る」と組む';
    if (text === 'より' && meaning === '起点') return '格助詞「より」(起点): 〜から。場所・時の起点';
    return null;
  }

  // ===== 接続助詞 =====
  if (pos === '接続助詞') {
    if (text === 'ば') return '接続助詞「ば」: 未然形＋ば=順接仮定 (もし〜ならば) ／ 已然形＋ば=順接確定 (〜ので・〜ところ・〜と)。前接形で意味が変わるので識別必須';
    if (text === 'ど' || text === 'ども') return '接続助詞「ど／ども」: 逆接確定 (〜けれども)。已然形接続。文意の転換点を示す';
    if (text === 'とも') return '接続助詞「とも」: 逆接仮定 (たとえ〜ても)。終止形接続 (形容詞は連用形)';
    if (text === 'て') return '接続助詞「て」: 順接 (〜て・〜のうえで)。連用形接続。動作の継起・並列を示す';
    if (text === 'して') return '接続助詞「して」: 並列・順接 (〜して)。「て」の強調';
    if (text === 'つつ') return '接続助詞「つつ」: 反復・継続・同時並行 (〜ながら・〜つつ)。連用形接続。動作の重ね目を示す';
    if (text === 'ながら') return '接続助詞「ながら」: 並行 (〜ながら) ／ 逆接 (〜のに・〜ものの)。動詞連用形 / 体言・形容詞語幹接続';
    if (text === 'を' || text === 'に') return '接続助詞「を／に」: 順接 (〜ので) ／ 逆接 (〜のに) ／ 単純接続 (〜と・〜ところ)。連体形接続。文脈で訳し分ける';
    if (text === 'が') return '接続助詞「が」: 逆接 (〜のに・〜が)。連体形接続。中世以降の用法';
    if (text === 'もの' || text === 'ものから' || text === 'ものゆゑ' || text === 'ものの') return '接続助詞「もの〜」型: 逆接 (〜けれども)。連体形接続';
    return null;
  }

  return null;
}

let added = 0;
let perCategory = { 助動詞: 0, 係助詞: 0, 副助詞: 0, 補助動詞: 0, 動詞: 0, 形容詞: 0, 形容動詞: 0, 副詞: 0, 代名詞: 0, 格助詞: 0 };
for (const s of data.sentences) {
  for (const tk of s.tokens) {
    if (tk.hint) continue;
    const h = buildHint(tk);
    if (h) {
      tk.hint = h;
      added++;
      const pos = (tk.grammarTag && tk.grammarTag.pos) || '';
      const ctype = (tk.grammarTag && tk.grammarTag.conjugationType) || '';
      const cat = pos || (ctype === 'ナリ' ? '形容動詞' : '');
      if (cat in perCategory) perCategory[cat]++;
    }
  }
}

